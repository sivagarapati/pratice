		<h1>MISSION</h1>
		
		<p>The main objective of the Vinay's Zoo Park is to complement and strengthen the national efforts in conservation of the rich biodiversity of the   country, particularly the wild fauna. To achieve this objective the following protocol is adopted:</p>
		<p>	a). Supporting the conservation of endangered species by attempting their coordinated breeding under ex-situ conditions and raise stocks for
		rehabilitating them in wild as and when it is appropriate and desirable.</p>
		<p>	b). To inspire amongst zoo visitors empathy for wild animals, an understanding and awareness about the need for conservation of natural resource
		and maintaining ecological balance.</p>
		<p>	c). Providing opportunities for scientific studies useful for conservation in general and creation of data base for sharing between the agencies
		involved in in-situ and ex-situ conservation.</p>
		<p>	d). To function as Rescue Centre for orphaned wild animals, subject to the availability of appropriate housing and upkeep infrastructure.</p>
		
		<h2>HISTORY</h2>
		
		<p>The American WildLife committee and some prominent nature lovers of kansas city, was gathered under the Chairmanship of Chief Professor to formulate a proposal for establishment of Zoo Park for saving wild life in Kansas city. The committee met on 19th August, 1993 and approved the site between Kansas city and Topeka for the purpose. It was also decided that the American Government will develop the park and hand it over in running condition to the Missouri Government. An official from New Jersey Forest Service Mr. M.C.Mahanthyi was assigned the duty on 1st October 1994 to setup this Zoo.By the end of 1996, construction had sufficiently advanced. The roads, waterways, moats, ponds and animal houses had been constructed over the Northern half of the area, lawns laid and plantation done. With the statement of establishment of a Zoo Park at Kansas City, gifts of animals started coming from American Government and individuals. They were kept in temporary Cages around the Zoo, an enclosed square courtyard built for temporary halt for travellers during the summer days. The collection comprising of Tigers, Leopards, Bears, Foxes, Monkeys, Deer, Antelope and many birds were finally transferred to their permanent enclosures. The Zoo Park was formally inaugurated on 1st April 1997 by Mr suleman dirk, Hon'ble Member of the American Government. This Park was named as  VVS ZOO park. At present this zoo is running Successfully and saved many animals from the hunters in the Forest.</p>
